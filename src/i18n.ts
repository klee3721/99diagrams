export type TranslationKey = keyof typeof translations.vi

export function isLanguage(value: unknown): value is Language {
  return languages.includes(value as Language)
}

export type TranslationValues = Record<string, number | string>

export function translate(language: Language, key: TranslationKey, values?: TranslationValues) {
  const message = translations[language][key]
  if (!values) return message
  return message.replace(/\{(\w+)\}/g, (match, name: string) => {
    const value = values[name]
    return value === undefined ? match : String(value)
  })
}

export function nextLanguage(language: Language): Language {
  return language === 'vi' ? 'en' : 'vi'
}
